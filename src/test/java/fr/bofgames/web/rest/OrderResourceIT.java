    @BeforeEach
    public void initTest() {
        order = createEntity(em);
    }

    @Test
    @Transactional
    public void createOrder() throws Exception {
        int databaseSizeBeforeCreate = orderRepository.findAll().size();

        // Create the Order
        restOrderMockMvc.perform(post("/api/orders")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(order)))
            .andExpect(status().isCreated());

        // Validate the Order in the database
        List<Order> orderList = orderRepository.findAll();
        assertThat(orderList).hasSize(databaseSizeBeforeCreate + 1);
        Order testOrder = orderList.get(orderList.size() - 1);
        assertThat(testOrder.getFirstName()).isEqualTo(DEFAULT_FIRST_NAME);
        assertThat(testOrder.getLastName()).isEqualTo(DEFAULT_LAST_NAME);
        assertThat(testOrder.getEmail()).isEqualTo(DEFAULT_EMAIL);
        assertThat(testOrder.getStreet()).isEqualTo(DEFAULT_STREET);
        assertThat(testOrder.getPostcode()).isEqualTo(DEFAULT_POSTCODE);
        assertThat(testOrder.getCity()).isEqualTo(DEFAULT_CITY);
        assertThat(testOrder.getCountry()).isEqualTo(DEFAULT_COUNTRY);
        assertThat(testOrder.getItems()).isEqualTo(DEFAULT_ITEMS);

        // Validate the Order in Elasticsearch
        verify(mockOrderSearchRepository, times(1)).save(testOrder);
    }

